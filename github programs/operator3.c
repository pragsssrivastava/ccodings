#include<stdio.h>
int main(){
	int a,b,c,d;
	printf("Enter the value of a:");
	scanf("%d",&a);
	printf("Enter the value of b:");
	scanf("%d",&b);
	printf("Enter the value of c:");
	scanf("%d",&c);
	printf("Enter the value of d:");
	scanf("%d",&d);
	a++;
	b--;
	++c;
	--d;
	printf("a++:%d\n",a++);
	printf("b--:%d\n",b--);
	printf("++c:%d\n",++c);
	printf("--d:%d\n",--d);
}

	
